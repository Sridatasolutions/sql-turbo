DROP TABLE IF EXISTS order_items CASCADE;
DROP TABLE IF EXISTS orders CASCADE;

CREATE TABLE orders (
    order_id        SERIAL PRIMARY KEY,
    customer_id     INT REFERENCES customers(customer_id),
    sales_rep_id    INT REFERENCES employees(employee_id),
    order_date      DATE NOT NULL,
    required_date   DATE,
    shipped_date    DATE,
    order_status    VARCHAR(20) CHECK (order_status IN ('Pending','Processing','Shipped','Delivered','Cancelled','Returned')),
    payment_status  VARCHAR(20) CHECK (payment_status IN ('Unpaid','Partial','Paid','Refunded')),
    payment_method  VARCHAR(30),
    shipping_method VARCHAR(30),
    shipping_cost   NUMERIC(10,2) DEFAULT 0,
    discount_pct    NUMERIC(5,2) DEFAULT 0,
    notes           TEXT,
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE order_items (
    order_item_id   SERIAL PRIMARY KEY,
    order_id        INT REFERENCES orders(order_id),
    product_id      INT REFERENCES products(product_id),
    quantity        INT NOT NULL DEFAULT 1,
    unit_price      NUMERIC(10,2) NOT NULL,
    discount_pct    NUMERIC(5,2) DEFAULT 0,
    line_total      NUMERIC(12,2) GENERATED ALWAYS AS (quantity * unit_price * (1 - discount_pct/100)) STORED
);

